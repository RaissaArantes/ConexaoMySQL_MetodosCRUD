    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Buscar TODOS os Membros
        DefaultTableModel dtmMembros = (DefaultTableModel) jTableMembros.getModel();
        MembrosDAO dao = new MembrosDAO();
        dtmMembros.setNumRows(0);
        try {
            for(Membros m : dao.Select()){
                Object[] dados = {m.getId(), m.getNome()};
                dtmMembros.addRow(dados);
            }
        } catch (ClassNotFoundException  | SQLException ex) {
            Logger.getLogger(Inicio.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Excluir Membro
        if(jTableMembros.getSelectedRow()!=-1){
            Membros mb = new Membros();
            MembrosDAO dao = new MembrosDAO();
            int linha = jTableMembros.getSelectedRow();
            int coluna = 0;
            Object obj = jTableMembros.getValueAt(linha, coluna);
            String str = String.valueOf(obj);
            int id = ConverteParaInt(str);
            mb.setId(id);
            try {
                dao.Delete(mb);
            } catch (ClassNotFoundException  | SQLException ex) {
                Logger.getLogger(Inicio.class.getName()).log(Level.SEVERE, null, ex);
            }
            DefaultTableModel dtmUsuarios = (DefaultTableModel) jTableMembros.getModel();
            dtmUsuarios.setNumRows(0);
            try {
                for(Membros m : dao.Select()){
                    Object[] dados = {m.getId(), m.getNome()};
                    dtmUsuarios.addRow(dados);
                }
            } catch (ClassNotFoundException  | SQLException ex) {
                Logger.getLogger(Inicio.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Alterar nome do Membro:
        Membros mem = new Membros();
        MembrosDAO dao = new MembrosDAO();
        int linha = jTableMembros.getSelectedRow();
        int tipo = 0;        
        Object obj = jTableMembros.getValueAt(linha,0);
        String str = String.valueOf(obj);
        mem.setId(ConverteParaInt(str));
        obj = jTableMembros.getValueAt(linha, 1);
        str = String.valueOf(obj);
        mem.setNome(str);
        try {
            dao.Update(mem);
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Inicio.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

